package samples;

public class Location {
	 private String name, email; // private = restricted access

	  // Getter
	  public String getName() {
	    return name;
	  }
	  
	  public String getEmail() {
		    return email;
		  }

	  // Setter
	  public void setName(String newName) {
	    this.name = newName;
	  }
	  
	  public void setEmail(String newEmail) {
		    this.name = newEmail;
		  }
}
